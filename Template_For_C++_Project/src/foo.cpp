#include "foo.hpp"



// ==== Foo::Constructor ======================================================
// 
// ============================================================================
Foo::Foo(const int inVal) : m_val(inVal)
{
} // end of "Foo::Constructor"



// ==== Foo::printFoo =========================================================
// 
// ============================================================================
void Foo::printFoo() const
{
  std::cout << "** printFoo() **" << std::endl;
  std::cout << "m_val: " << m_val;
} // end of "Foo::printFoo"



// ==== Foo::printFoo =========================================================
// 
// ============================================================================
const int Foo::getVal() const
{
  return m_val;
} // end of "Foo::getVal"



// ==== Foo::Overloaded << Operator ===========================================
// 
// ============================================================================
std::ostream& operator<<(std::ostream& out, const Foo& inFoo)
{
  inFoo.printFoo();
  return out;
} // end of "Foo::Overloaded << Operator"
